// MARK: - GradientLayer
public func createGradientLayer(with colors: [UIColor], frame: CGRect) -> CAGradientLayer {
    let gradientLayer = CAGradientLayer()
    gradientLayer.frame = frame
    
    // Convert colors to CGColor
    let cgColors = colors.map { $0.cgColor }
    
    // Set gradient colors
    gradientLayer.colors = cgColors
    
    // Calculate the step size for locations and start/end points based on the number of colors
    let stepSize = 1.0 / Double(colors.count - 1)

    // Set gradient locations
    gradientLayer.locations = (0..<colors.count).map { NSNumber(value: Double($0) * stepSize) }
    
    // Set gradient start and end points based on the number of colors
    gradientLayer.startPoint = CGPoint(x: .zero, y: 0.5)
    gradientLayer.endPoint = CGPoint(x: CGFloat(colors.count - 1) * CGFloat(stepSize), y: 0.5)
    return gradientLayer
}
